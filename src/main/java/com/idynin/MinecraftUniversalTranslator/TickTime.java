package com.idynin.MinecraftUniversalTranslator;

public class TickTime {
	private final static long ticksPerSecond = 20;
	public final static long SECOND = ticksPerSecond;
	public final static long MINUTE = SECOND * 60;
	public final static long FIVEMINUTES = MINUTE * 5;
	public final static long FIFTEENMINUTES = FIVEMINUTES * 3;
	public final static long HALFHOUR = FIFTEENMINUTES * 2;
	public final static long HOUR = HALFHOUR * 2;
}
